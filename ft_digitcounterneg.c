#include "libft.h"

int		ft_digitcounterneg(int num)
{
	int	res;

	res = num < 0 ? 1 : 0;
	if (num == 0)
		return (1);
	while (num != 0)
	{
		res++;
		num /= 10;
	}
	return (res);
}
