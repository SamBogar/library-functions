int		ft_issqr(int n)
{
	int	i;
	int	prev_num;

	prev_num = 0;
	i = 0;
	while (1)
	{
		if (i * i == n)
			return (1);
		if (i * i > n && prev_num * prev_num < n)
			return (0);
		prev_num = i;
		i++;
	}
	return (0);
}
