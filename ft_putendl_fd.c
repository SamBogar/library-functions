#include "libft.h"

void		ft_putendl_fd(char const *s, int fd)
{
	size_t	i;

	i = 0;
	if (s)
		while (i < ft_strlen(s))
		{
			write(fd, &s[i], 1);
			i++;
		}
	write(fd, "\n", 1);
}
